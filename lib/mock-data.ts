const getImagesForType = (type: string): string[] => {
  const lowerType = type.toLowerCase()

  if (lowerType.includes("căn hộ")) return realEstateImages.apartment
  if (lowerType.includes("biệt thự")) return realEstateImages.villa
  if (lowerType.includes("nhà")) return realEstateImages.house
  if (lowerType.includes("đất")) return realEstateImages.land
  if (lowerType.includes("penthouse")) return realEstateImages.penthouse
  if (lowerType.includes("văn phòng") || lowerType.includes("office")) return realEstateImages.office

  // Mặc định trả về hình ảnh căn hộ
  return realEstateImages.apartment
}

